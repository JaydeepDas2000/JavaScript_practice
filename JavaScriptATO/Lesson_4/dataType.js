// typeof keyword is use to know the data type of that varibles

let x = 7;
console.log(typeof x); // It will show 'number'

let y = true;
console.log(typeof y); // it will show 'boolean'

let z = 'Hello World';
console.log(typeof z); // it will show 'string'

let a;
console.log(a); // it will show 'undefined' value
console.log(typeof a); // it will also show 'undefined' datatype


